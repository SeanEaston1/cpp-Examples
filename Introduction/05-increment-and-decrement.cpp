#include <iostream>
using namespace std;

int main()
{
    
    //This is increment//
    
    int a=10 , b=5, c;
    
    c=++a*b;
    cout<<c<<endl;
    cout<<a<<endl;
    
    //This is decrement//
    
    int d=20, e=25, f;
    
    f=d++*e;
    cout<<f<<endl;
    cout<<d<<endl;
    
    return 0;
}
